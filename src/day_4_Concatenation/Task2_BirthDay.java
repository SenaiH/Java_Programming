package day_4_Concatenation;

public class Task2_BirthDay {
    public static void main(String[] args) {
        String name = "Jhon";
        int birthDay = 24;
        String birthMonth = "October";
        int birthYear = 1988;

        System.out.println(name + " was born on " + birthMonth + "/" + birthDay + "/" + birthYear);
    }

}
