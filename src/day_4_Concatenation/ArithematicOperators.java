package day_4_Concatenation;

public class ArithematicOperators {
    public static void main(String[] args) {
        System.out.println(100+200);//int
        System.out.println(30-10);//int
        System.out.println(50/6);//int
        System.out.println(50.0/5);//double
        System.out.println(50/5.0);//double
        System.out.println(50.0/5.0);//double
        System.out.println(50d/5);//double
        System.out.println((int)(2.5/0.5));// Casting in int.
        System.out.println((double)(100%13));//casting to double
        System.out.println(100%13);

        //System.out.println(9/0);
    }


}
