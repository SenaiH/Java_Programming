package day_02_escapesequence;

public class Escape_Sequence {
    public static void main(String[] args) {
        System.out.println("We are studying\nJava Programming");

        System.out.println("____________________________________________");

        System.out.println("\t Hello Everbody!");
        System.out.println("\t\t Hello Everbody!");

        System.out.println("____________________________________________");

        System.out.println("\\");
        System.out.println("\\\\");

        System.out.println("____________________________________________");

        System.out.println("My favorite Tv show is \"Prison Break\"");
    }

}
/*
\n:New Line.
\t:Paragraph Space.
\\:Prints one backslash.
\":Prints double quotation mark.
 */